
interface WeatherData {
  main: {
    temp: number;
    feels_like: number;
    humidity: number;
    pressure: number;
  };
  weather: Array<{
    main: string;
    description: string;
    icon: string;
  }>;
  wind: {
    speed: number;
  };
  name: string;
  sys: {
    country: string;
  };
}

// Generate realistic weather data for Indian cities
const generateWeatherData = (cityName: string, baseTemp: number, condition: string): WeatherData => {
  const conditions = {
    "Clear": { main: "Clear", description: "clear sky", icon: "01d" },
    "Clouds": { main: "Clouds", description: "scattered clouds", icon: "03d" },
    "Rain": { main: "Rain", description: "light rain", icon: "10d" },
    "Thunderstorm": { main: "Thunderstorm", description: "thunderstorm", icon: "11d" }
  };

  return {
    main: {
      temp: baseTemp,
      feels_like: baseTemp + Math.floor(Math.random() * 6) - 2,
      humidity: 60 + Math.floor(Math.random() * 30),
      pressure: 1010 + Math.floor(Math.random() * 15)
    },
    weather: [conditions[condition as keyof typeof conditions] || conditions.Clear],
    wind: { speed: 2 + Math.random() * 4 },
    name: cityName,
    sys: { country: "IN" }
  };
};

